/**
 * Exports every carousel slide as Instagram feed–friendly PNGs (1080×1350, 4:5).
 * Output: marketing/output/carousel-instagram/rXX-slug/slide-NN.png
 *
 * Requires: npx playwright install chromium
 * Run: npm run export-carousel-slides
 */

import { createServer } from "node:http";
import { existsSync, mkdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { chromium } from "playwright";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, "..");
const outRoot = path.join(__dirname, "output", "carousel-instagram");

/** Slide counts must match carousels-all.html REELS */
const REEL_EXPORT = [
  { id: 1, slug: "18-years-their-wealth", count: 9 },
  { id: 2, slug: "no-big-idea-required", count: 7 },
  { id: 3, slug: "brand-not-money", count: 5 },
  { id: 4, slug: "hard-work-not-rich", count: 7 },
  { id: 5, slug: "one-step-ahead", count: 5 },
  { id: 6, slug: "buyers-over-views", count: 5 },
  { id: 7, slug: "zero-start-product-first", count: 5 },
  { id: 8, slug: "pick-one-let-it-run", count: 8 },
];

function sendFile(res, fp) {
  if (!existsSync(fp) || !statSync(fp).isFile()) {
    res.writeHead(404);
    res.end("Not found");
    return;
  }
  const ext = path.extname(fp).toLowerCase();
  const ct =
    ext === ".html"
      ? "text/html; charset=utf-8"
      : ext === ".png"
        ? "image/png"
        : ext === ".webp"
          ? "image/webp"
          : ext === ".svg"
            ? "image/svg+xml"
            : ext === ".js"
              ? "text/javascript; charset=utf-8"
              : "application/octet-stream";
  res.writeHead(200, { "Content-Type": ct, "Cache-Control": "no-store" });
  res.end(readFileSync(fp));
}

const server = createServer((req, res) => {
  const u = new URL(req.url ?? "/", "http://127.0.0.1");
  let pathname = decodeURIComponent(u.pathname);

  if (pathname.startsWith("/images/")) {
    const fp = path.join(repoRoot, "public", "images", pathname.slice(8));
    return sendFile(res, fp);
  }
  if (pathname.startsWith("/marketing/")) {
    const fp = path.join(repoRoot, "marketing", pathname.slice(11));
    return sendFile(res, fp);
  }
  res.writeHead(404);
  res.end();
});

const port = await new Promise((resolve, reject) => {
  server.listen(0, "127.0.0.1", () => resolve(server.address().port));
  server.on("error", reject);
});

mkdirSync(outRoot, { recursive: true });
writeFileSync(
  path.join(outRoot, "README.txt"),
  `Strategic Sloth — carousel PNGs for Instagram feed

Each folder is one script (carousel post). Slides are numbered in order.

Format: 1080 × 1350 px (4:5) — recommended for Instagram multi-image posts in the feed.
Upload all slides in one folder as a single carousel; keep order (slide-01 first).

Generated by: npm run export-carousel-slides
`,
  "utf8"
);

const base = `http://127.0.0.1:${port}`;
let browser;
let ctx;
let page;

try {
  browser = await chromium.launch({ headless: true });
  ctx = await browser.newContext({
    viewport: { width: 1080, height: 1350 },
    deviceScaleFactor: 1,
  });
  page = await ctx.newPage();

  let total = 0;
  for (const reel of REEL_EXPORT) {
    const dir = path.join(outRoot, `r${String(reel.id).padStart(2, "0")}-${reel.slug}`);
    mkdirSync(dir, { recursive: true });

    for (let s = 0; s < reel.count; s++) {
      const url = `${base}/marketing/carousels-all.html?export=1&reelId=${reel.id}&slideIdx=${s}`;
      await page.goto(url, { waitUntil: "load", timeout: 120000 });
      await page.waitForFunction(() => document.fonts.status === "loaded", { timeout: 30000 });
      await page.waitForSelector("#ig-export-root .stage", { timeout: 15000 });
      await page.locator("#ig-export-root").screenshot({
        path: path.join(dir, `slide-${String(s + 1).padStart(2, "0")}.png`),
        type: "png",
      });
      total += 1;
      process.stdout.write(`\rWrote ${total} slides…`);
    }
    console.log(`\n${dir}`);
  }

  console.log(`\nDone. ${total} PNGs under ${outRoot}`);
} catch (e) {
  console.error(e);
  process.exitCode = 1;
} finally {
  server.close();
  if (ctx) await ctx.close().catch(() => {});
  if (browser) await browser.close().catch(() => {});
}
